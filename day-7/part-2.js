function generateAllPossibleCombinations(length) {
    const operators = ['+', '*', '|']
    const results = []

    function backdrop(combination) {
        if(combination.length === length) {
            results.push(combination.join(''))
            return
        }

        for(let op of operators) {
            combination.push(op)
            backdrop(combination)
            combination.pop()
        }
    }

    backdrop([])
    return results
}

function evaluate(numbers, ops) {
    let result = numbers[0]

    for (let i = 0; i < ops.length; i++) {
        let operator = ops[i]
        let nextNumber = numbers[i+1]

        if (operator === '+') {
            result = result + nextNumber
        } else if (operator === '*') {
            result = result * nextNumber
        } else if (operator === '|') {
            result = Number(result.toString() + nextNumber.toString())
        }
    }

    return result
}

function findTotalCorrect(data) {

    let total = 0

    for(let key in data) {
        const numbers = data[key]

        if(numbers.length === 1) {
            if(numbers[0] === Number(key)) {
                total += key
                continue;
            }
        }

        const allOperatorCombinations = generateAllPossibleCombinations(numbers.length - 1)

        let canAchieve = false

        for(let ops of allOperatorCombinations) {
            const result = evaluate(numbers, ops)

            if(result === Number(key)) {
                canAchieve = true
            }
        }

        if(canAchieve) {
            total += Number(key)
        }
    }


    return total
}

