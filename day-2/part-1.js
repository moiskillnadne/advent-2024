const processedInput = input.split("\n").map((level) => level.split(" ").map(str => Number(str)))

// Rule 1: All increase or all decrease
// Rule 2: Gap between 1 and 3

let safeLevels = 0

const validateSafeGap = (a,b) => {
    const diff = Math.abs( a - b)

    const minGap = 1
    const maxGap = 3

    return minGap <= diff && diff <= maxGap
}

for (let level of processedInput) {

    let isIncrease = null

    const gapsSafety = []

    for (let i = 0; i < level.length; i++) {
        const currentLevel = level[i]
        const nextLevel = level[i + 1]

        if(!nextLevel) {
            break;
        }

        const diff = currentLevel - nextLevel
        const isVectorIncreasing = diff > 0

        if (i === 0 && isIncrease === null) {
            isIncrease = isVectorIncreasing

            const isSafeGap = validateSafeGap(currentLevel, nextLevel)

            gapsSafety.push(isSafeGap)
            continue
        }

        const isSafeGap = validateSafeGap(currentLevel, nextLevel)

        const isSameVector = isIncrease === isVectorIncreasing

        const isSafe = isSafeGap && isSameVector

        gapsSafety.push(isSafe)
    }

    const isSafeLevel = gapsSafety.every((gapCheck) => gapCheck === true)

    if(isSafeLevel) {
        safeLevels += 1
    }
}

